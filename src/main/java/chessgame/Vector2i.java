package chessgame;

/**
 * @author Naumov V.V.
 * Class for representing a vector of integers
 */
public class Vector2i {
    public int x;
    public int y;

    /**
     * Constructor of the class
     * @param x x coordinate
     * @param y y coordinate
     */
    public Vector2i(int x, int y){
        this.x = x;
        this.y = y;
    }
}
