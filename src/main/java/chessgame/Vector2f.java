package chessgame;


/**
 * Class for representing a vector of floats
 * @author Naumov V.V.
 */
public class Vector2f {
    public float x;
    public float y;

    /**
     * Constructor of the class
     * @param x x coordinate
     * @param y y coordinate
     */
    public Vector2f(float x, float y){
        this.x = x;
        this.y = y;
    }
}
